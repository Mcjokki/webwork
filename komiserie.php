<h1>Friends</h1>
<h2><p>Friends er en serie som har 10 sesonger. I løpet av disse 10 årene så følger mann vennegjengen Monica, Rachel, Phoebe, Chandler, Joey og Ross. De er alle i midt 20årene i serien når den starter. Alle har ulike mål og ønsker i livet, og hver episode gir noen morsomme øyeblikk. Friends er en en serie som er anbefalt for alle. Friends er også en av de mest populære seriene som er laget og går fortsatt på Netflix og andre tv kanaler selv om det var ferdig i 2004 (16år siden). Alle bør vertfall gi denne serien en sjanse dersom en ikke har sett det enda.  Friends har mange oppturer og nedturer igjennom serien, for å gi en kjapp oppsummering av karakterene så har du: Chandler Bing en sarkastisk person som alltid kommer med gode kommentarer. Han er ikke spesielt glad i jobben sin og bor med sin bestevenn Joey. For Joey Tribbiani så er han en god venn å ha siden han bryr seg veldig om de rundt seg, han er også ekstremt glad i mat og damer. Joey er en amatør skuespiller som prøver å slå igjennom som Skuespiller i New York City. Ross Geller er nerden i gruppen og har DR i paleontologi, Ross har vært favoritten til sine foreldre hele sin oppvekst og har formet et tett bånd med søsteren sin Monica. Monica er kokken i gruppen, flink til å lage mat og var overvektig som barn. Monica sitt store ønske er å bli en Mor og få annerkjennelse fra sine egne foreldre. Rachel Green oppvokst i en rik familie som har gjort henne bortskjemt, men forlot forloveden sin ved alteret. Hun må klare seg selv så flytter inn med Monica for å kunne vokse opp og ta ansvar for eget liv. Phoebe har den mest kompliserte og spesielle barndommen av de alle, faren forlot henne tidlig og moren tok selvmord. Hun velger å bli massør samtidig spille gitar for folk med veldig få kunnskaper om gitar. 
    Friends har den perfekte samlingen av karakterer og en kjemi som er vanskelig å slå. Absolutt anbefalt terningkast 5.
