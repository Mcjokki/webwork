<h1>Mad Men</h1>
<h2><p>Mad men tar oss tilbake til 1960 tallet. Noe av det som virkelig viser hvor dedikert de er når de laget serien er hvor detaljert de er i forhold til hvordan ting var på starten av 60tallet. I 7 sesonger så følger vi den kreative direktøren for et reklame firma. Han lever den amerikanske drømmen med hus, kone og barn. Don Draper er en karakter som er veldig inntresang å følge siden han har ikke vært den samme siden han kom tilbake fra Korea krigen. Don Draper prøver å komme seg videre i livet men fortiden hans tar innpå han. Mad Men har en rekke fantastiske karakterer vi følger. Peggy Olsen ei karriere drivene kvinne som ikke vil bli som mange andre kvinner. Pete Cambel en ung og sulten mann som ønsker å bli noe stort samtidig som han ønsker å fylle skoene kulturen ser på mannfolk. Roger Sterling senior navnepartner som ikke vil bli eldre og klamrer seg fast til sin ungdom samtidig som han lever i nuet. Det er  mange karakterer å følge som har stor betydning for serien men, Mad Men skal ha for den fantastiske jobben de har gjort ved å få deg til ønske du levde på 60 tallet. 
